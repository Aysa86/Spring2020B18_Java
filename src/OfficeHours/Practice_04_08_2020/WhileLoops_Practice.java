package OfficeHours.Practice_04_08_2020;

public class WhileLoops_Practice {
    public static void main(String[] args) {

        int a = 0;
         while(a < 5){
             System.out.println("Hello Cybertek");
             a++;
         }

         int b = 0;
         while (b <= 100){
             if(b % 15 == 0){
                 System.out.print(b+ " ");
             }
             b++;
         }


    }
}
