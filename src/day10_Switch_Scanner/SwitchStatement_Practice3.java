package day10_Switch_Scanner;

public class SwitchStatement_Practice3 {
    public static void main(String[] args) {

        char ch = 'a';
        String result = "";
        switch (ch){
            case'A':
                result = "A";//System.out.println("A");
                break;
            case 'B':
                result = "B";//System.out.println("B");
                break;
            case 'C':
                result = "C";//System.out.println("C");
                break;
            case 'D':
                result = "D";//System.out.println("D");
                break;

            default:
                result = "Invalid";//System.out.println("Invalid");
                break;




        }





    }
}
